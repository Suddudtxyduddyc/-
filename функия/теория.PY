# функция - блок кода, который выполняется в момент вызова
# если функцию не вызвать, она не запустится

# создаётся таким образом:
# def название_функции()

# вывзывается функция таким образом:
# название_функции()
# внутрь скобок функции передаются АРГУМЕНТЫ
# чтобы передать функции аргумент надо написать его в скобках при вызове
# название_функции("аргумент")
# чтобы функция могла принять аргумент надо придумать ему название (как переменной) и указать в скобках при создании функции
# def название_функции(аргумент)