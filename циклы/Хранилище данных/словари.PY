# словарь является итерируемым объектом (состоит из элементов)
# каждым элементом словаря явлется ПАРА (ключ и значение)
# ключи не могут повторяться - они должны быть уникальны
# элементы отделяются друг от друга запятыми
# ключ и значение отделяются друг от друга двоеточием
# словарь записывается в фигурных скобках
сотрудники={"Николай:менеджер"}
#добавить пару
сотрудники["светлана"]="бугхалтер"
#добавить пару с уже существующим ключом нельзя-это действие обновит значение у существуещего ключа
сотрудники["светлана"]="секретарь"
#запросить значение по ключу
сотрудники.get("светлана")=="секретарь"
сотрудники.get("сергей")==None
#удалить последний элемент
сотрудники.popitem()==("светлана","Секретарь")
сотрудники.keys()=={"николай"}
сотрудники.values()==["менеджер"]

